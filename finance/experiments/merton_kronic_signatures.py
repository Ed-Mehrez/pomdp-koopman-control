r"""
Cumulative Path Signatures Controller for the Merton Portfolio Problem
======================================================================

This script implements Solution A from the theory document: the Cumulative
Path Signatures Controller.

It demonstrates that by replacing the localized RBF kernel with a polynomial
basis natively generated by the level-2 log-signature of the state path,
the Koopman generator strictly and globally bounds the objective function,
completely solving the extrapolation hallucination problem without need for
online correction.

Key features:
1. Streaming Chen-Normalized Signature computation for Z = (log W, pi, V).
2. Explicit analytic evaluation of the generator L on the signature features.
3. Offline EDMD on the signature basis.
4. Correct global extrapolation of the variance penalty.
"""


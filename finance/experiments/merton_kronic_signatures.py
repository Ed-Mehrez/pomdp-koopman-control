import torch
import signatory

class SignatoryFeatureExtractor:
    def __init__(self, depth=3, device='cpu'):
        self.depth = depth
        self.device = device
        
        # d=3 -> Lead-Lag d=6
        self.sig_dim = signatory.signature_channels(6, depth)
        
    def extract(self, paths):
        """
        paths: torch.Tensor of shape (B, L, 3)
        Returns: (B, sig_dim + 1)
        """
        B, L, D = paths.shape
        
        # CRITICAL: Prepend a zero-basepoint before lead-lag.
        # Translates path increments into absolute position anchors.
        basepoint = torch.zeros((B, 1, D), device=self.device, dtype=paths.dtype)
        paths = torch.cat([basepoint, paths], dim=1)
        
        repeated = paths.repeat_interleave(2, dim=1)
        lead = repeated[:, :-1, :]
        lag = repeated[:, 1:, :]
        ll_path = torch.cat([lead, lag], dim=2)
        
        # Scale down to prevent variance blowup at depth 3
        ll_path[:, :, 0] /= 5.0
        ll_path[:, :, 1] /= 5.0
        ll_path[:, :, 2] /= 5.0
        
        s = signatory.signature(ll_path, self.depth)
        bias = torch.ones((B, 1), device=self.device, dtype=paths.dtype)
        return torch.cat([bias, s], dim=1)


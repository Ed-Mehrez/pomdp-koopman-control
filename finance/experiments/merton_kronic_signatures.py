# 3. Signature KGEDMD
# =============================================================================

class SignatureEDMD:
    def __init__(self, dt):
        self.dt = dt
        self.A_T = None
        self.eigenvectors = None
        self.eigenvalues = None
        
    def fit(self, psi_X, psi_Y):
        """
        psi_X: (N, features) at time t
        psi_Y: (N, features) at time t+dt
        """
        print("    [EDMD] Computing G00 and G10...")
        N = len(psi_X)
        G00 = (psi_X.T @ psi_X) / N
        G10 = (psi_X.T @ psi_Y) / N
        
        print("    [EDMD] Solving Eigenvalue Problem...")
        discrete_A, _, _, _ = lstsq(G00 + 1e-4 * np.eye(G00.shape[0]), G10)
        
        eigenvals, eigenvecs = eig(discrete_A)
        
        # Logarithmic mapping to continuous generator A_T 
        # lambda_c = ln(lambda_d) / dt
        valid_idx = np.abs(eigenvals) > 1e-8
        cont_eigenvals = np.zeros_like(eigenvals, dtype=complex)
        cont_eigenvals[valid_idx] = np.log(eigenvals[valid_idx]) / self.dt
        
        self.A_T = discrete_A # We will just use the discrete operator natively mapped to horizon!
        self.eigenvalues = cont_eigenvals
        self.eigenvectors = eigenvecs

# =============================================================================
# 4. KRONIC Controller
# =============================================================================

class SignatureKRONICMerton:
    def __init__(self, env, horizon=None):
        self.env = env
        self.dt = 1/252
        self.horizon = horizon if horizon is not None else self.dt
        self.device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
        self.extractor = SignatoryFeatureExtractor(depth=3, device=self.device)
        self.edmd = SignatureEDMD(self.dt)
        self.w_utility = None
        
    def generate_training_data(self, n_paths=100, n_steps=252):
        print(f"2. Generating Path Signature Training Data (Lead-Lag)...")
        psi_X = []
        psi_Y = []
        U_all = []
        
        # We process path by path to build the shifted datasets
        with torch.no_grad():
            for path in range(n_paths):
                log_W = np.random.normal(0, 0.1)
                pi_val = np.random.uniform(0.1, 1.2)
                V_val = np.random.uniform(0.02, 0.08)
                
                state_traj = [[log_W, pi_val, V_val]]
                
                for t in range(n_steps):
                    state = state_traj[-1]
                    action = 0.02 * np.random.randn()
                    next_state, _ = self.env.step(state, action, dt=self.dt)
                    
                    state_traj.append(list(next_state))
                    
                    if len(state_traj) >= 4:
                        # Fixed size sliding windows!
                        p_X = np.array(state_traj[-4:-1])
                        p_Y = np.array(state_traj[-3:])
                        
                        # Wealth Translation Invariance: center logW on current time
                        shift_W = p_X[-1, 0]  # logW at end of path_X
                        p_X[:, 0] -= shift_W
                        p_Y[:, 0] -= shift_W
                        
                        path_X = torch.tensor([p_X], dtype=torch.float64, device=self.device)
                        path_Y = torch.tensor([p_Y], dtype=torch.float64, device=self.device)
                        
                        f_X = self.extractor.extract(path_X)[0].cpu().numpy()
                        f_Y = self.extractor.extract(path_Y)[0].cpu().numpy()
                        
                        psi_X.append(f_X)
                        psi_Y.append(f_Y)
                        
                        # Utility evaluated on SHIFTED wealth (centered at current time)
                        # W_shifted = exp(shifted_logW) where shifted baseline is 0
                        W_s = np.exp(p_Y[-1, 0])  # = exp(Δ logW from current)
                        U = (W_s**(1 - self.env.gamma)) / (1 - self.env.gamma)
                        U_all.append(U)
                        
        return np.array(psi_X), np.array(psi_Y), np.array(U_all)
        
    def train(self, psi_X, psi_Y, U_array):
        print("3. Fitting Signature EDMD...")
        self.edmd.fit(psi_X, psi_Y)
        
        print("4. Projecting Utility onto psi_Y (growth-rate utility)...")
        # Train w on psi_Y so that w·ψ_Y ≈ U(exp(ΔlogW))
        # At inference: w · A · ψ_X predicts this via Koopman evolution
        ridge = Ridge(alpha=1.0)
        ridge.fit(psi_Y, U_array)
        self.w_utility = ridge.coef_
        
        r2 = ridge.score(psi_Y, U_array)
        print(f"   Utility Projection R² (on psi_Y): {r2:.4f}")

    def find_optimal_pi(self, history_traj, pi_candidates):
        """
        history_traj: A list of state lists up to the current time t.
        We peek ahead to evaluate pi_candidates.
        """
        steps = max(1, int(self.horizon / self.dt))
        # Discrete transition operator for the horizon
        discrete_A_horizon = np.linalg.matrix_power(self.edmd.A_T, steps)
        
        B = len(pi_candidates)
        
        # Wealth Translation Invariance: center logW on current time
        shift_W = history_traj[-1][0]
        shifted_history = []
        for state in history_traj:
            shifted_history.append([state[0] - shift_W, state[1], state[2]])
        
        path_X = torch.tensor(shifted_history, dtype=torch.float64, device=self.device)
        path_X = path_X.unsqueeze(0).repeat(B, 1, 1) # (B, 3, 3)
        path_X[:, -1, 1] = torch.tensor(pi_candidates, dtype=torch.float64, device=self.device)
        
        with torch.no_grad():
            psi_0s = self.extractor.extract(path_X).cpu().numpy()
        
        Psi_T_preds = psi_0s @ discrete_A_horizon
        EU_objs = np.sum(self.w_utility * Psi_T_preds, axis=1)
        
        best_idx = np.argmax(EU_objs)
        return pi_candidates[best_idx], EU_objs

    def debug_generator(self):
        print(f"\n--- Generator Built Successfully: {self.extractor.sig_dim + 1} dimensions ---")


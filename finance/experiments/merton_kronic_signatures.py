def run_signature_experiment():
    print("=" * 60)
    print("Cumulative Path Signatures KRONIC for Merton-Heston")
    print("=" * 60)
    
    env = HestonMertonEnv()
    kronic = SignatureKRONICMerton(env, horizon=0.1)
    
    psi_X, psi_Y, U_array = kronic.generate_training_data(100, 252)
    kronic.train(psi_X, psi_Y, U_array)
    kronic.debug_generator()
    
    print("\n[Phase 2] Evaluating Global Extrapolation Accuracy")
    
    # Needs a baseline trajectory to evaluate from (simulating we just started watching)
    # Give it a 3-step burn-in so Lead-Lag signature isn't trivial
    V_test = 0.05
    W0 = 1.0
    history_traj = [
        [np.log(W0), 0.1, V_test],
        [np.log(W0), 0.1, V_test],
        [np.log(W0), 0.1, V_test]
    ]
    
    pi_candidates = np.linspace(0.1, 10.0, 200)
    
    pi_opt, objs = kronic.find_optimal_pi(history_traj, pi_candidates)
    merton_opt = env.merton_optimal(V_test)
    
    print(f"  Analytical Target Optimum : {merton_opt:.3f}")
    print(f"  Signature KRONIC Optimum  : {pi_opt:.3f}")
    
    plt.figure(figsize=(8, 6))
    plt.plot(pi_candidates, objs, 'b-', linewidth=2, label='Signature KRONIC Expected Utility')
    plt.axvline(merton_opt, color='k', linestyle='--', label='Analytical Max')
    plt.xlabel(r'Candidate Allocation ($\pi$)')
    plt.ylabel('Expected Utility')
    plt.title('Global Extrapolation Bounding using Cumulative Signatures')
    plt.legend()
    plt.grid(True, alpha=0.3)
    
    plt.annotate("Notice how the utility \nglobally plummets for high $\pi$.\nNo Hallucination!", 
                 xy=(pi_opt*2, objs.min()), xytext=(5.0, objs.max()*0.8),
                 arrowprops=dict(facecolor='black', shrink=0.05),
                 fontsize=10, bbox=dict(boxstyle="round4", fc="w"))
    
    plt.tight_layout()
    plt.savefig('finance/experiments/merton_kronic_signatures_bounded.png', dpi=150)
    print("\nSaved plot to finance/experiments/merton_kronic_signatures_bounded.png")
    
if __name__ == "__main__":
    run_signature_experiment()

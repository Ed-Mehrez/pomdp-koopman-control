# GP fitting per stage
# ===========================================================================


def flatten_stage(z: np.ndarray, u_grid: np.ndarray, y_matrix: np.ndarray):
    r"""Flatten (n_seeds, d_z) x (n_grid,) x (n_seeds, n_grid) into:
        X:  (n_seeds*n_grid, d_z + 1)
        y:  (n_seeds*n_grid,)
    """
    n_seeds, d_z = z.shape
    n_grid = u_grid.size
    X = np.zeros((n_seeds * n_grid, d_z + 1))
    y = np.zeros(n_seeds * n_grid)
    for k in range(n_seeds):
        for j in range(n_grid):
            X[k * n_grid + j, :d_z] = z[k]
            X[k * n_grid + j, d_z] = u_grid[j]
            y[k * n_grid + j] = y_matrix[k, j]
    return X, y


def _standardized_length_scale(X: np.ndarray) -> np.ndarray:
    r"""Robust length-scale heuristic for joint (z, u) with features of very
    different scales.

    The raw median-heuristic length scale `ls_d = median(|x_d^i - x_d^j|)`
    collapses on features whose pairwise differences are orders of
    magnitude smaller than others (e.g. EWMA(r^2) at 1e-6 scale).  The
    resulting kernel over-localizes in those dimensions and the GP
    overfits to the path-feature axis.

    Robust alternative: standardize each column to unit std and return the
    median-heuristic in standardized coordinates, then UN-standardize so
    the returned length scales can be applied to the original features:

        ls_d = std_d * median_heuristic_on_standardized.

    With a single length scale across all standardized features, each
    feature contributes equally on its own scale.  Floors prevent exact
    zero variance on constant columns.
    """
    X = np.atleast_2d(X)
    stds = np.std(X, axis=0, ddof=1)
    stds = np.where(stds > 1e-10, stds, 1.0)
    X_std = X / stds
    ls_std = median_heuristic_length_scale(X_std)
    # Use a single scalar length scale (geometric mean) so different feature
    # scales don't dominate; then un-standardize per-feature.
    ls_scalar = float(np.exp(np.mean(np.log(np.maximum(ls_std, 1e-6)))))
    return stds * ls_scalar


def fit_gp_stage(
    z_train: np.ndarray,
    u_grid: np.ndarray,
    y_matrix: np.ndarray,
    ridge_alpha: float,
) -> Tuple[GPResidualModel, RBFKernel, np.ndarray]:
    r"""Fit GP on joint (z, u) with robust (standardized) length scales.

    Uses the standardized-median-heuristic below to avoid collapse on
    small-scale path features.
    """
    X, y = flatten_stage(z_train, u_grid, y_matrix)
    ls = _standardized_length_scale(X)
    kernel = RBFKernel(length_scale=ls)
    model = GPResidualModel(kernel=kernel, alpha=ridge_alpha).fit(
        X, y, standardize=True,
    )
    return model, kernel, ls



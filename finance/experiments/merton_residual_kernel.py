@dataclass
class TrainingData:
    z_A: np.ndarray       # (n_seeds, 2)
    z_B: np.ndarray       # (n_seeds, 4)
    u_grid: np.ndarray    # (n_grid,)
    y_matrix: np.ndarray  # (n_seeds, n_grid) paired Delta log W
    V_at_decision: np.ndarray  # (n_seeds,)
    logW_0_at_decision: np.ndarray  # for diagnostics
    logW_T_matrix: np.ndarray  # (n_seeds, n_grid) absolute terminal logW
    theta_env: float


def collect_training_data(
    env: HestonMertonEnv,
    cfg: ExperimentConfig,
    n_seeds: int,
    base_seed: int,
    verbose: bool = False,
) -> TrainingData:
    u_grid = np.linspace(-cfg.u_max, cfg.u_max, cfg.n_grid)
    n_total_steps = cfg.n_burn_steps + cfg.n_dec_steps
    z_A = np.zeros((n_seeds, 2))
    z_B = np.zeros((n_seeds, 4))
    V_at_decision = np.zeros(n_seeds)
    logW_0_at_decision = np.zeros(n_seeds)
    y_matrix = np.zeros((n_seeds, cfg.n_grid))
    logW_T_matrix = np.zeros((n_seeds, cfg.n_grid))

    # Separate RNG for V_0 sampling so it's independent of the noise stream.
    v0_rng = np.random.RandomState(base_seed)

    for k in range(n_seeds):
        V0 = float(v0_rng.uniform(cfg.V0_low, cfg.V0_high))
        noise = _paired_noise_stream(cfg.rho, n_total_steps, base_seed + 1 + k)
        burn_noise = noise[:cfg.n_burn_steps]
        dec_noise = noise[cfg.n_burn_steps:]

        burn_out = _burn_in_with_myopic(env, cfg, V0, burn_noise)
        V_dec = burn_out["V_at_decision"]
        logW_dec = burn_out["logW_at_decision"]
        V_at_decision[k] = V_dec
        logW_0_at_decision[k] = logW_dec

        # Stage A features: [log(V/theta), tau_frac].  tau_frac = 1 at start
        # of decision horizon.
        z_A[k] = [np.log(max(V_dec, 1e-8) / env.theta), 1.0]
        # Stage B features: Stage A + [ewma_r, ewma_r2]
        z_B[k] = [
            np.log(max(V_dec, 1e-8) / env.theta),
            1.0,
            burn_out["ewma_r"],
            burn_out["ewma_r2"],
        ]

        # For each u in the grid, run constant-u decision rollout with the
        # SAME decision-horizon noise (CRN across u within seed).
        for j, u in enumerate(u_grid):
            logW_T = _decision_rollout_logW(
                env, cfg, V_dec, logW_dec, float(u), dec_noise,
            )
            logW_T_matrix[k, j] = logW_T
        # Paired response: delta logW = logW_T(u) - logW_T(0)
        idx0 = int(np.argmin(np.abs(u_grid - 0.0)))
        y_matrix[k] = logW_T_matrix[k] - logW_T_matrix[k, idx0]

        if verbose and (k + 1) % max(1, n_seeds // 4) == 0:
            print(f"  collected {k+1}/{n_seeds} seeds")
    return TrainingData(
        z_A=z_A, z_B=z_B, u_grid=u_grid,
        y_matrix=y_matrix,
        V_at_decision=V_at_decision,
        logW_0_at_decision=logW_0_at_decision,
        logW_T_matrix=logW_T_matrix,
        theta_env=env.theta,
    )



r"""
Heston myopic-reference residual-kernel controller.

Architecture (following docs/theory_kronic_extrapolation.md Section 5.3):
    1. state input  z_t
    2. reference    pi_ref(V_hat)  =  (mu - r) / (gamma * V_hat)
    3. overlay      pi_t           =  pi_ref * (1 + u),    u in [-u_max, u_max]
    4. residual     y(z, u)        =  log W_T(u) - log W_T(0)   (PAIRED, per seed)
    5. GP model     y ~ f(z, u) + N(0, alpha)    (RBF kernel on joint (z, u))
    6. decision     reference-abstention EI rule on u-grid per query state

Response-variable choice (explicit, not silent)
-----------------------------------------------
We target PAIRED log-wealth improvement y(seed, u) = logW_T(u; seed) - logW_T(0; seed),
not CRRA terminal utility.

When is log W_T(u) exactly quadratic in u?  (Precise statement.)
----------------------------------------------------------------
The statement "log W_T(u) is exactly quadratic in u per seed" holds under
ALL of the following assumptions together; any of them failing weakens
the claim to piecewise-quadratic or only approximately quadratic.

    (A1) Constant-u multiplicative overlay:   pi_t = (1+u) * pi_ref(V_t),
         with u scalar and held constant for the entire decision horizon.
    (A2) The reference policy pi_ref(V_t) is EVALUATED at the SAME V_t
         path across every u in the grid.  In particular, the V_t path is
         generated by the SAME noise realization independent of u.
    (A3) All decision-horizon noise (dB_W, dB_V) is shared across u-values
         within a seed (CRN).  The noise realization itself does not
         depend on u.
    (A4) pi_ref(V) is continuously differentiable in V on the V-path --
         in particular, no discontinuous floor/ceiling clipping fires
         during the rollout.

Under (A1)-(A4), one can write per path
    log W_T(u)  =  (T r + A - 0.5 B + N) + u * (A - B + N) + u**2 * (-0.5 B),
    A := integral_0^T pi_m * (mu - r) dt
    B := integral_0^T pi_m**2 * V_t dt
    N := integral_0^T pi_m * sqrt(V_t) dB_W
so the PAIRED residual y(u; path) = log W_T(u; path) - log W_T(0; path)
is exactly
    y(u; path)  =  u * (A + N - B)  +  u**2 * (-0.5 B),
no intercept, quadratic in u per path.

Assumption (A4) does NOT hold strictly in this script:  pi_ref uses a
V_floor clip (default 0.005) to bound the leverage implied by the
Merton-myopic formula at low V.  When V dips below the floor during
a rollout, pi_ref is constant in V on that sub-interval instead of
varying as 1/V, and the per-path response is piecewise-quadratic rather
than strictly quadratic.  In practice the floor fires on a small minority
of paths; we report it as a modeling assumption rather than hiding it.

Across seeds A, B, N are random path functionals; the pooled regression
therefore fits MEAN coefficients plus path-dispersion residuals.  A GP
on joint (z, u) explains part of that dispersion through state features.

Kelly vs CRRA interpretation
----------------------------
    * argmax_u E[y(u; seed)] is the Kelly (log-utility) residual optimum.
    * It is NOT the exact CRRA-gamma residual optimum.  For CRRA gamma>1
      the target is argmax_u ( E[log W_T] - 0.5*(gamma-1)*Var[log W_T] ),
      which combines the mean quadratic with a separate variance
      quadratic under a lognormal approximation.  That pathway is
      implemented in merton_relative_overlay_bayesian.py.
    * The reference policy pi_ref(V) = (mu - r) / (gamma V) remains
      CRRA-gamma.  This kernel residual therefore learns:
        "how to adjust the CRRA-myopic reference for Kelly-residual
         improvement."
      This is a hybrid target; the outputs below are labeled "Kelly
      residual" throughout.

State stages (sensitivity test, one script)
-------------------------------------------
Stage A (minimal, theory-motivated):
    z_A  =  [ log(V_hat_t / theta),  (T_dec - t_dec) / T_dec ]

Stage B (small path-aware add-ons):
    z_B  =  z_A  +  [ EWMA(log_return),  EWMA(log_return^2) ]
    with EWMA computed over a burn-in period using the myopic policy.
    These are cheap model-free path features; if Stage B beats Stage A
    materially, richer path information is worth the cost.

Stage C (recurrent signature / EFM) is NOT implemented in this script.
It is deferred until A-vs-B shows a meaningful gap.

Baselines (all evaluated on held-out seeds under paired noise)
--------------------------------------------------------------
    * myopic_only       : u = 0 always
    * bayes_local_quad  : state-agnostic Bayesian local quadratic
                          (reference_abstention_ei rule)
    * residual_kernel_A : GP on z_A, x (z_A, u)
    * residual_kernel_B : GP on z_B, x (z_B, u)
"""


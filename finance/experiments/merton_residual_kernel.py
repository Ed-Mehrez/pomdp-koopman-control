@dataclass
class ControllerEval:
    name: str
    recommended_u: np.ndarray          # (n_test_seeds,)
    abstention_flags: np.ndarray       # bool, True if u==0
    realized_logW: np.ndarray          # paired rollout logW at recommended u
    improvement_over_myopic: np.ndarray  # per seed
    rationales: List[str]


def evaluate_controller(
    env: HestonMertonEnv,
    cfg: ExperimentConfig,
    test_data: TrainingData,
    policy_fn: Callable[[int, TrainingData], Tuple[float, str, bool]],
    name: str,
) -> ControllerEval:
    r"""For each test seed, query the controller's recommended u at the
    decision time, then run CRN rollout at that u; compare to myopic (u=0).
    """
    # Re-generate the decision noise per seed so we can run a test rollout
    # at the recommended u (this noise path was already used at training
    # time for each u in the grid; here we re-use it for whatever u the
    # controller recommends, which maintains CRN alignment with myopic).
    n_seeds = test_data.y_matrix.shape[0]
    recommended = np.zeros(n_seeds)
    abstention = np.zeros(n_seeds, dtype=bool)
    realized = np.zeros(n_seeds)
    improvement = np.zeros(n_seeds)
    rationales = []
    v0_rng = np.random.RandomState(1_000_000 + hash(name) % 100)  # fresh but deterministic
    # We don't actually need V_0 again because the test_data.V_at_decision is
    # already captured from the burn-in.  We DO need the decision-horizon
    # noise to re-roll at the controller's chosen u.  Since we
    # regenerate it here based on test seed indices, CRN is preserved
    # across controllers as long as they use the same test-data and seed
    # bookkeeping.  (test_data records were built with base_seed stored in
    # its construction; we use a shared test-seed convention below.)
    # Simpler approach: test_data already stores logW_T_matrix at every u
    # in the grid.  If the controller's recommended u is a grid point,
    # just index into that matrix.  If not, we would need to re-roll;
    # this implementation RESTRICTS recommendations to the grid.
    for k in range(n_seeds):
        u_rec, rationale, abstain = policy_fn(k, test_data)
        # Snap to nearest grid point
        idx = int(np.argmin(np.abs(test_data.u_grid - u_rec)))
        u_on_grid = float(test_data.u_grid[idx])
        recommended[k] = u_on_grid
        abstention[k] = abstain
        realized[k] = test_data.logW_T_matrix[k, idx]
        idx0 = int(np.argmin(np.abs(test_data.u_grid - 0.0)))
        improvement[k] = test_data.logW_T_matrix[k, idx] - test_data.logW_T_matrix[k, idx0]
        rationales.append(rationale)
    return ControllerEval(
        name=name,
        recommended_u=recommended,
        abstention_flags=abstention,
        realized_logW=realized,
        improvement_over_myopic=improvement,
        rationales=rationales,
    )



def controller_myopic_only(u_grid: np.ndarray):
    def pol(k, test_data):
        return 0.0, "myopic (u=0)", True
    return pol


def controller_bayes_local_quadratic(
    train_data: TrainingData, cfg: ExperimentConfig,
):
    r"""State-agnostic Bayesian local quadratic using all pooled (u, y) data."""
    u_pooled = np.tile(train_data.u_grid, train_data.y_matrix.shape[0])
    y_pooled = train_data.y_matrix.flatten()
    prior = NIGPrior(
        mu_0=np.zeros(3),
        V_0=np.diag([10.0, 10.0, 10.0]),
        a_0=2.0, b_0=1.0,
    )
    post = fit_bayesian_quadratic(u_pooled, y_pooled, prior=prior)
    summary = summarize_posterior(
        post, u_max=cfg.u_max,
        n_grid=cfg.n_grid, n_samples=6000,
        rng=np.random.RandomState(13),
    )
    rec_action = summary.recommended_action
    rationale = summary.recommended_action_rationale
    abstain = abs(rec_action) < 1e-8

    def pol(k, test_data):
        return float(rec_action), rationale, abstain
    return pol, summary


def controller_residual_kernel_stage(
    train_data: TrainingData, cfg: ExperimentConfig,
    stage: str = "A",
    poi_threshold: float = 0.7,
    lcb_alpha: float = 0.1,
):
    r"""KELLY-residual controller: fit one GP on (z_stage, u) -> Delta logW
    and use posterior EI with reference abstention.

    Target: E[Delta logW | z, u] (maximizes log-utility / Kelly growth).
    This is NOT the CRRA gamma > 1 target; see controller_residual_kernel_crra_stage.
    """
    if stage == "A":
        z_train = train_data.z_A
    elif stage == "B":
        z_train = train_data.z_B
    else:
        raise ValueError(f"unknown stage: {stage}")

    model, kernel, ls = fit_gp_stage(
        z_train, train_data.u_grid, train_data.y_matrix,
        ridge_alpha=cfg.rbf_ridge_alpha,
    )
    d_z = z_train.shape[1]
    u_grid = train_data.u_grid

    def pol(k, test_data):
        z_query = test_data.z_A[k] if stage == "A" else test_data.z_B[k]
        X_q = np.zeros((u_grid.size, d_z + 1))
        X_q[:, :d_z] = z_query[None, :]
        X_q[:, d_z] = u_grid
        mean, var = model.posterior(X_q, include_noise=False)
        decision = select_action_with_abstention(
            u_grid, mean, var,
            rule="reference_abstention_ei",
            baseline_value=0.0,  # y(z, 0) = 0 by construction
            baseline_u=0.0,
            poi_threshold=poi_threshold,
        )
        abstain = abs(decision.action) < 1e-8
        return decision.action, decision.rationale, abstain
    return pol, model, ls


def controller_residual_kernel_crra_stage(
    train_data: TrainingData, cfg: ExperimentConfig,
    stage: str = "A",
    gamma: float = 3.0,
    poi_threshold: float = 0.7,
    n_posterior_samples: int = 4000,
):
    r"""CRRA-gamma residual controller via TWO-GP combination.

    Target (lognormal approximation):
        CRRA_score(z, u)  =  E[Delta logW | z, u]
                           -  0.5 * (gamma - 1) * Var(Delta logW | z, u).

    Fits two independent GPs on pooled (z, u) training data:
        GP_mean : target = y_i         -> posterior on E[y | z, u]
        GP_sq   : target = y_i ** 2    -> posterior on E[y^2 | z, u]

    At query z_query we take marginal posterior samples from each GP at
    the u-grid, compute per-sample
        mu_k     ~  GP_mean(z_query, u)
        Ey2_k    ~  GP_sq(z_query, u)
        Var_k    =  max(Ey2_k - mu_k^2, 0)
        score_k  =  mu_k - 0.5 * (gamma - 1) * Var_k
    and feed the empirical posterior mean and variance of `score_k` to
    the generic abstention rule.  The rule's baseline is Q(u=0)=0, which
    is exact for this residual target (paired on log W).

    Change of target (explicit):
        Kelly residual:  argmax E[Delta logW] -- boundary-seeking at gamma>1.
        CRRA  residual:  argmax (E - 0.5*(gamma-1)*Var) -- interior optimum,
                         state-dependent through both E and Var.
    """
    if stage == "A":
        z_train = train_data.z_A
    elif stage == "B":
        z_train = train_data.z_B
    else:
        raise ValueError(f"unknown stage: {stage}")

    # Fit GP on y (mean target)
    X_tr, y_tr = flatten_stage(z_train, train_data.u_grid, train_data.y_matrix)
    ls = _standardized_length_scale(X_tr)
    gp_mean = GPResidualModel(
        kernel=RBFKernel(length_scale=ls), alpha=cfg.rbf_ridge_alpha,
    ).fit(X_tr, y_tr, standardize=True)
    # Fit GP on y^2 (second-moment target)
    gp_sq = GPResidualModel(
        kernel=RBFKernel(length_scale=ls), alpha=cfg.rbf_ridge_alpha,
    ).fit(X_tr, y_tr ** 2, standardize=True)

    d_z = z_train.shape[1]
    u_grid = train_data.u_grid
    lam_risk = 0.5 * (float(gamma) - 1.0)

    def pol(k, test_data):
        z_query = test_data.z_A[k] if stage == "A" else test_data.z_B[k]
        X_q = np.zeros((u_grid.size, d_z + 1))
        X_q[:, :d_z] = z_query[None, :]
        X_q[:, d_z] = u_grid

        rng = np.random.RandomState(2_000_000 + k)
        mu_samples = sample_marginal_posterior(
            gp_mean, X_q, n_samples=n_posterior_samples, rng=rng,
        )
        ey2_samples = sample_marginal_posterior(
            gp_sq, X_q, n_samples=n_posterior_samples,
            rng=np.random.RandomState(3_000_000 + k),
        )
        var_samples = np.maximum(ey2_samples - mu_samples ** 2, 0.0)
        score_samples = mu_samples - lam_risk * var_samples
        score_mean = score_samples.mean(axis=0)
        score_var = score_samples.var(axis=0, ddof=1)

        decision = select_action_with_abstention(
            u_grid, score_mean, score_var,
            rule="reference_abstention_ei",
            baseline_value=0.0,  # CRRA score at u=0 equals 0 by construction
            baseline_u=0.0,
            poi_threshold=poi_threshold,
        )
        abstain = abs(decision.action) < 1e-8
        return decision.action, decision.rationale, abstain

    return pol, gp_mean, gp_sq, ls



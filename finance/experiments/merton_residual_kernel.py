def _summarize_controller_eval(
    ev: ControllerEval,
    target: Optional[RiskSensitiveTarget] = None,
    n_boot: int = 2000,
    rng: Optional[np.random.RandomState] = None,
):
    r"""Held-out summary.

    If `target` is provided, the primary metric is the risk-sensitive
    target's scalar evaluation with bootstrap SE; mean and variance of the
    paired Δ log W are also returned as raw-moment diagnostics so the
    mean-vs-variance decomposition is visible.
    """
    imp = ev.improvement_over_myopic
    n = imp.size
    mean_imp = float(np.mean(imp))
    var_imp = float(np.var(imp, ddof=1)) if n > 1 else 0.0
    se_imp = float(np.sqrt(var_imp / n)) if n > 1 else float("nan")
    abst_rate = float(np.mean(ev.abstention_flags))
    frac_active = 1.0 - abst_rate
    if np.sum(~ev.abstention_flags) > 1:
        u_active = ev.recommended_u[~ev.abstention_flags]
        u_med = float(np.median(u_active))
        u_p05, u_p95 = float(np.percentile(u_active, 5)), float(np.percentile(u_active, 95))
        u_std = float(np.std(u_active, ddof=1))
    else:
        u_med = u_p05 = u_p95 = u_std = float("nan")
    out = {
        "name": ev.name,
        "n": n,
        "mean_improvement": mean_imp,
        "var_improvement": var_imp,
        "se_improvement": se_imp,
        "abstention_rate": abst_rate,
        "active_rate": frac_active,
        "u_rec_median": u_med,
        "u_rec_p05": u_p05,
        "u_rec_p95": u_p95,
        "u_rec_std": u_std,
    }
    if target is not None:
        result = target.evaluate_with_bootstrap(imp, n_boot=n_boot, rng=rng)
        out["risk_point"] = result["point"]
        out["risk_se"] = result["boot_se"]
        out["risk_q05"] = result["boot_q05"]
        out["risk_q95"] = result["boot_q95"]
        out["risk_target_name"] = target.name
        out["risk_target_gamma"] = target.gamma
    return out



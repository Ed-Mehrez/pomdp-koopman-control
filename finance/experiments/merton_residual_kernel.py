def _plot_crra_posteriors_per_stage(
    test_data: TrainingData,
    cfg: ExperimentConfig,
    gp_mean_A: GPResidualModel, gp_sq_A: GPResidualModel,
    gp_mean_B: GPResidualModel, gp_sq_B: GPResidualModel,
    out_path: str,
    query_indices=(0, 10, 20),
):
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    u_grid = test_data.u_grid
    lam_risk = 0.5 * (cfg.gamma - 1.0)
    fig, axes = plt.subplots(
        1, len(query_indices), figsize=(5 * len(query_indices), 4.5), sharey=True,
    )
    if len(query_indices) == 1:
        axes = [axes]

    def _score_posterior(gp_mean, gp_sq, X_q, n_samples=4000, seed=11):
        rng = np.random.RandomState(seed)
        mu = sample_marginal_posterior(gp_mean, X_q, n_samples, rng=rng)
        ey2 = sample_marginal_posterior(
            gp_sq, X_q, n_samples, rng=np.random.RandomState(seed + 1)
        )
        var = np.maximum(ey2 - mu ** 2, 0.0)
        score = mu - lam_risk * var
        return score.mean(axis=0), score.std(axis=0, ddof=1)

    for ax, qi in zip(axes, query_indices):
        # Stage A
        z_A = test_data.z_A[qi]
        X_A = np.zeros((u_grid.size, 3))
        X_A[:, :2] = z_A[None, :]
        X_A[:, 2] = u_grid
        mA, sA = _score_posterior(gp_mean_A, gp_sq_A, X_A)
        # Stage B
        z_B = test_data.z_B[qi]
        X_B = np.zeros((u_grid.size, 5))
        X_B[:, :4] = z_B[None, :]
        X_B[:, 4] = u_grid
        mB, sB = _score_posterior(gp_mean_B, gp_sq_B, X_B)

        ax.axhline(0.0, color="gray", lw=0.6)
        ax.axvline(0.0, color="gray", lw=0.6, linestyle=":")
        ax.plot(u_grid, mA, "b-", lw=1.8, label="Stage A  CRRA score")
        ax.fill_between(u_grid, mA - 2 * sA, mA + 2 * sA, color="b", alpha=0.15)
        ax.plot(u_grid, mB, "r-", lw=1.8, label="Stage B  CRRA score")
        ax.fill_between(u_grid, mB - 2 * sB, mB + 2 * sB, color="r", alpha=0.15)
        V_q = test_data.V_at_decision[qi]
        ax.set_title(
            f"test seed {qi}  V̂={V_q:.3f}  log(V̂/θ)={z_A[0]:+.2f}  "
            f"EWMA(r²)={z_B[3]:+.2e}",
            fontsize=9,
        )
        ax.set_xlabel("u (multiplicative overlay)")
        ax.legend(fontsize=8, loc="best")
        ax.grid(alpha=0.3)
    axes[0].set_ylabel(f"CRRA score(u) ≈ E[ΔlogW] − 0.5(γ−1)·Var(ΔlogW),  γ={cfg.gamma}")
    fig.suptitle(
        "Residual-kernel CRRA posterior over overlay u, per test-seed state",
        fontsize=11,
    )
    fig.tight_layout()
    fig.savefig(out_path, dpi=130)
    plt.close(fig)



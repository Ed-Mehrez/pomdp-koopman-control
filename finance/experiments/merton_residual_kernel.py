@dataclass(frozen=True)
class ExperimentConfig:
    rho: float = -0.7
    gamma: float = 3.0
    V_floor_for_pi_ref: float = 0.005
    T_burn: float = 0.5        # years
    T_dec: float = 1.0         # years (decision horizon)
    dt: float = 1.0 / 252.0
    u_max: float = 0.3
    n_grid: int = 13
    n_train_seeds: int = 100
    n_test_seeds: int = 80
    V0_low: float = 0.02
    V0_high: float = 0.08
    ewma_halflife_days: float = 21.0   # ~ 1 month
    rbf_ridge_alpha: float = 5e-3

    @property
    def n_burn_steps(self) -> int:
        return int(round(self.T_burn / self.dt))

    @property
    def n_dec_steps(self) -> int:
        return int(round(self.T_dec / self.dt))

    @property
    def ewma_lam(self) -> float:
        return float(np.log(2.0) / max(self.ewma_halflife_days, 1e-3))


# ===========================================================================
# Rollout helpers
# ===========================================================================


def _paired_noise_stream(
    rho: float, n_steps: int, seed: int
) -> np.ndarray:
    rng = np.random.RandomState(seed)
    zA = rng.standard_normal(n_steps)
    zB = rng.standard_normal(n_steps)
    z1 = zA
    z2 = rho * zA + np.sqrt(max(1.0 - rho * rho, 0.0)) * zB
    return np.column_stack([z1, z2])


def _pi_ref(env: HestonMertonEnv, V: float, V_floor: float) -> float:
    return float(env.merton_optimal(max(V, V_floor)))


def _burn_in_with_myopic(
    env: HestonMertonEnv,
    cfg: ExperimentConfig,
    V0: float,
    noise: np.ndarray,
) -> Dict[str, float]:
    r"""Run myopic policy for n_burn_steps to seed path features.

    Returns: dict of path features at end of burn-in AND V_at_decision.
    """
    logW = 0.0
    V = float(V0)
    lam = cfg.ewma_lam
    rho_e = float(np.exp(-lam * cfg.dt))
    ewma_r = 0.0
    ewma_r2 = 0.0
    for t in range(cfg.n_burn_steps):
        pi_m = _pi_ref(env, V, cfg.V_floor_for_pi_ref)
        logW_new, V_new = env.step_explicit(
            logW, V, pi_m,
            float(noise[t, 0]), float(noise[t, 1]), cfg.dt,
        )
        r_t = logW_new - logW  # log-return
        ewma_r = rho_e * ewma_r + (1.0 - rho_e) * r_t
        ewma_r2 = rho_e * ewma_r2 + (1.0 - rho_e) * (r_t * r_t)
        logW, V = logW_new, V_new
    return {
        "V_at_decision": V,
        "logW_at_decision": logW,
        "ewma_r": ewma_r,
        "ewma_r2": ewma_r2,
    }


def _decision_rollout_logW(
    env: HestonMertonEnv,
    cfg: ExperimentConfig,
    V_start: float,
    logW_start: float,
    u: float,
    noise: np.ndarray,
) -> float:
    r"""Constant-u multiplicative rollout over the decision horizon.

    Returns log W at end of decision horizon.
    """
    logW = float(logW_start)
    V = float(V_start)
    for t in range(cfg.n_dec_steps):
        pi_m = _pi_ref(env, V, cfg.V_floor_for_pi_ref)
        pi_t = (1.0 + float(u)) * pi_m
        logW, V = env.step_explicit(
            logW, V, pi_t,
            float(noise[t, 0]), float(noise[t, 1]), cfg.dt,
        )
    return float(logW)



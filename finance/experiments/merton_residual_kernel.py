# ===========================================================================
# Risk-sensitive target ladder  (adapter-level abstraction)
# ===========================================================================
#
# The generic kernel residual core in src/control/kernel_residual_control.py
# is response-agnostic.  The choice of risk-sensitive objective (what scalar
# we summarize a distribution of paired improvements by) is an adapter-level
# concern and lives here.
#
# The ladder, from least to most numerically delicate:
#
#   * "mean_only":   E[y]
#       - Risk-neutral / Kelly residual diagnostic.
#
#   * "mean_variance":   E[y] - 0.5 * (gamma - 1) * Var[y]
#       - Lognormal / CRRA-gamma approximation.  Coincides with
#         log-certainty-equivalent improvement under lognormal wealth.
#         This is what the two-GP CRRA controller OPTIMIZES; it is also
#         the right held-out evaluation metric for that controller.
#
#   * "entropic":   -(1/gamma) * log E[exp(-gamma * y)]
#       - Exponential-utility certainty equivalent; exact for normal y,
#         approximately CRRA for small y.  Numerically stable via log-sum-exp.
#
#   * "exact_crra" (not implemented here):
#       E[U(W_T(u)) - U(W_T(0))] with U = W^(1-gamma)/(1-gamma).
#       Numerically unstable at gamma > 1 (polynomial tails).
#
# We implement the first three.  All scalar; the ladder is extensible.


RiskTargetName = Literal["mean_only", "mean_variance", "entropic"]


@dataclass(frozen=True)
class RiskSensitiveTarget:
    r"""Adapter-level risk-sensitive objective evaluator.

    name:   one of the ladder names above
    gamma:  risk-aversion parameter (used by mean_variance and entropic)
    """
    name: RiskTargetName
    gamma: float = 1.0

    def evaluate(self, y_samples: np.ndarray) -> float:
        y = np.asarray(y_samples, dtype=float).flatten()
        if self.name == "mean_only":
            return float(np.mean(y))
        if self.name == "mean_variance":
            var = float(np.var(y, ddof=1)) if y.size > 1 else 0.0
            return float(np.mean(y) - 0.5 * (self.gamma - 1.0) * var)
        if self.name == "entropic":
            # CE = -1/gamma * log E[exp(-gamma y)]; log-sum-exp for stability.
            if self.gamma <= 0.0:
                raise ValueError("entropic requires gamma > 0")
            z = -self.gamma * y
            m = float(np.max(z))
            return float(-(1.0 / self.gamma) * (m + np.log(np.mean(np.exp(z - m)))))
        raise ValueError(f"unknown risk target: {self.name}")

    def evaluate_with_bootstrap(
        self,
        y_samples: np.ndarray,
        n_boot: int = 2000,
        rng: Optional[np.random.RandomState] = None,
    ) -> Dict[str, float]:
        r"""Point estimate plus bootstrap SE and 90% interval."""
        if rng is None:
            rng = np.random.RandomState(0)
        y = np.asarray(y_samples, dtype=float).flatten()
        point = self.evaluate(y)
        n = y.size
        boot = np.zeros(n_boot)
        for b in range(n_boot):
            idx = rng.randint(0, n, size=n)
            boot[b] = self.evaluate(y[idx])
        return {
            "point": point,
            "boot_se": float(np.std(boot, ddof=1)),
            "boot_q05": float(np.quantile(boot, 0.05)),
            "boot_q95": float(np.quantile(boot, 0.95)),
        }


